package OperasiMatematika;

public class Augmented {
    public static void main(String[] args) {
        //Augmented Assignments

        int c = 20;

        c += 10; //c = c + 10
        System.out.println(c);
        c -= 10; //c = c - 10
        System.out.println(c);
        c *= 10; //c = c * 10
        System.out.println(c);
        c /= 10; //c = c / 10
        System.out.println(c);
        c %= 10; //c = c % 10
        System.out.println(c);
    }
}
