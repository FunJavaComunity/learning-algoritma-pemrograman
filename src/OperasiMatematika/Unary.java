package OperasiMatematika;

public class Unary {
    public static void main(String[] args) {
        //Unary Operator
        int d = 50;


        //Increment
        d++;
        d++;
        System.out.println("Increment = " + d);

        //decrement
        d--;
        System.out.println("Decrement = " + d);
    }
}
