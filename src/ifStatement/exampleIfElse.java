package ifStatement;

public class exampleIfElse {
    public static void main(String[] args) {
        int skor = 69;

        if (skor >= 70){
            System.out.println("Selamat anda lulus ujian!");
        } else {
            System.out.println("Maaf, Anda tidak lulus ujian.");
        }

        System.out.println("Selesai");
    }
}
